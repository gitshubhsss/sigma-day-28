//use the switch statements to print the day of the week using a number variable 'day' with values 0 to 7

let dayNumber = 7;

switch (dayNumber) {
  case 1:
    console.log("monday");
    break;

  case 2:
    console.log("tuesday");
    break;

  case 3:
    console.log("Wednesday");
    break;

  case 4:
    console.log("thursday");
    break;

  case 5:
    console.log("friday");
    break;

  case 6:
    console.log("saturday");
    break;

  case 7:
    console.log("sunday");
    break;
}
